Once I focused on the core DSA patterns everything started to make sense. Here are the patterns that cover most interview questions:

1. Two Pointers
↳ Used for sorted arrays & strings (pair sum, removing duplicates, merging in place).

2. Fast & Slow Pointers
↳ Cycle detection, middle of linked list, detecting starting point of cycle.

3. Sliding Window 
↳ Longest / shortest subarray or substring under constraints (k-distinct, frequency limits).

4. Prefix Sum
↳ Subarray sums, range queries, difference arrays.

5. Monotonic Stack / Queue
↳ Next greatest element, stock span, daily temperatures, sliding window max

6. Merge Intervals
↳ Overlapping intervals, meeting rooms, merging ranges.

7. Sorting + Sweep Line
↳ Event processing, scheduling, interval counting, greedy decisions.

8. Divide & Conquer
↳ Merge sort, quicksort, finding medians, recursive splitting.

9. Binary Search (All Variants)
↳ Sorted arrays, rotated arrays, “search on answer” for thresholds or minima.

10. Greedy Algorithms
↳ Activity selection, gas station, optimal scheduling & choice making.

11. Linked List Techniques
↳ Reversal, merging, reordering

12. Heaps & Top-K
↳ Priority queues, top-k elements, streaming median, smallest/largest k.

13. Hashmaps & Frequency Counting
↳ Anagrams, duplicates, sliding window character maps.

14. Dynamic Programming
↳ 1D/2D DP, subsequences, knapsack, DP on strings.

15. Backtracking / Recursive Search
↳ Permutations, subsets, combinations, N-Queens, search trees.

16. Graph Traversals (BFS & DFS)
↳ Connected components, islands, shortest path (unweighted), cycle detection.

17. Topological Sort (DAG Ordering)
↳ Course schedule, dependency resolution, task ordering.

18. Binary Tree Traversals (DFS/BFS)
↳ Preorder, inorder, postorder, level order traversal patterns.

19. Tree Path Problems
↳ Root-to-leaf sums, path sum, DFS with backtracking.

20. Trie (Prefix Tree)
↳ Autocomplete, prefix matching, dictionary search, word problems.

Once you understand these patterns, leetcode starts to make a lot more sense.

If you’re starting today:
→ Learn core DSA: arrays, hashmaps, stacks, queues, linked lists, graphs and trees.
→ Tackle one pattern at a time. Do 2–4 problems per pattern.
→ Create a study plan & revisit old problems as you learn new ones.
→ When ready, take on Blind75 questions. 